export const THEMES: readonly ThemeDefinition[] = [
  githubDark,
  oneDarkPro,
  dracula,
  tokyoNight,
  githubLight,
  catppuccinLatte,
];

export const DEFAULT_THEME_ID = githubDark.id;

const LEGACY_THEME_IDS: Readonly<Record<string, string>> = {
  "dark-modern": "github-dark",
  "one-dark": "one-dark-pro",
  "light-modern": "github-light",
  "solarized-light": "catppuccin-latte",
};

export function getTheme(id: string | null | undefined): ThemeDefinition {
  const resolvedId = id ? (LEGACY_THEME_IDS[id] ?? id) : DEFAULT_THEME_ID;
  return THEMES.find((theme) => theme.id === resolvedId) ?? githubDark;
}
