export const THEME_FAMILIES: readonly ThemeFamilyDefinition[] = [
  {
    id: "midnight",
    name: "Midnight",
    description: "Focused navy for terminal work",
    themes: { light: midnightLight, dark: midnightDark },
  },
  {
    id: "graphite",
    name: "Graphite",
    description: "Neutral gray with crisp hierarchy",
    themes: { light: graphiteLight, dark: graphiteDark },
  },
  {
    id: "dracula",
    name: "Dracula",
    description: "Deep purple with vivid terminal accents",
    themes: { light: draculaLight, dark: draculaDark },
  },
];

export const THEMES: readonly ThemeDefinition[] = THEME_FAMILIES.flatMap(
  (family) => [family.themes.light, family.themes.dark],
);

export const DEFAULT_THEME_FAMILY_ID = "midnight";
export const DEFAULT_THEME_MODE: ThemeMode = "dark";
export const DEFAULT_THEME_ID = midnightDark.id;

const LEGACY_THEME_IDS: Readonly<Record<string, string>> = {
  "github-dark": "midnight-dark",
  "dark-modern": "midnight-dark",
  "one-dark-pro": "graphite-dark",
  "one-dark": "graphite-dark",
  dracula: "dracula-dark",
  "tokyo-night": "midnight-dark",
  "github-light": "midnight-light",
  "light-modern": "midnight-light",
  "catppuccin-latte": "dracula-light",
  "solarized-light": "dracula-light",
};

export function getTheme(id: string | null | undefined): ThemeDefinition {
  const resolvedId = id ? (LEGACY_THEME_IDS[id] ?? id) : DEFAULT_THEME_ID;
  return THEMES.find((theme) => theme.id === resolvedId) ?? midnightDark;
}

export function getThemeFamily(
  id: string | null | undefined,
): ThemeFamilyDefinition {
  return THEME_FAMILIES.find((family) => family.id === id) ?? THEME_FAMILIES[0];
}

export function resolveTheme(
  preference: ThemePreference,
  systemAppearance: ThemeAppearance,
): ThemeDefinition {
  const appearance =
    preference.mode === "system" ? systemAppearance : preference.mode;
  return getThemeFamily(preference.familyId).themes[appearance];
}

export function preferenceFromThemeId(id: string): ThemePreference {
  const theme = getTheme(id);
  return { familyId: theme.familyId, mode: theme.appearance };
}
